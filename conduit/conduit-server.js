function parseFormToSiteData(body, subdomain) {
  const coords = [1,2,3,4,5,6].map(i=>({
    key: body[`coord${i}key`]||'', value: body[`coord${i}val`]||''
  })).filter(c=>c.key&&c.value);

  const sections = [1,2,3,4].map(si=>{
    const title = body[`sec${si}title`]||'';
    if (!title) return null;
    const narratives = [1,2,3].map(ni=>({
      tag:     body[`sec${si}n${ni}tag`]||'',
      year:    body[`sec${si}n${ni}year`]||'',
      heading: body[`sec${si}n${ni}heading`]||'',
      body1:   body[`sec${si}n${ni}body1`]||'',
      body2:   body[`sec${si}n${ni}body2`]||'',
    })).filter(n=>n.heading);
    return { num: body[`sec${si}num`]||`0${si}`, title, narratives };
  }).filter(Boolean);

  const footerLinks = [1,2,3].map(i=>({
    label: body[`fl${i}label`]||'', url: body[`fl${i}url`]||''
  })).filter(l=>l.label&&l.url);

  return {
    subdomain,
    siteTitle:      body.siteTitle||'',
    studioNum:      body.studioNum||'',
    metaDesc:       body.metaDesc||'',
    eyebrow:        body.eyebrow||'',
    heroTitle:      body.heroTitle||'',
    heroTitleGhost: body.heroTitleGhost||'',
    heroTagline:    body.heroTagline||'',
    coords, sections,
    terminalPrompt: body.terminalPrompt||'',
    terminalCmd:    body.terminalCmd||'',
    footerLogo:     body.footerLogo||'',
    footerLinks,
  };
}

