function esc(s) {
  return String(s||'').replace(/&/g,'&amp;').replace(/</g,'&lt;').replace(/>/g,'&gt;').replace(/"/g,'&quot;');
}

function postTelegram(text) {
  return new Promise((resolve, reject) => {
    const body = JSON.stringify({ chat_id: TELEGRAM_CHAT_ID, text });
    const req = https.request(
      `https://api.telegram.org/bot${TELEGRAM_TOKEN}/sendMessage`,
      { method: 'POST', headers: { 'Content-Type': 'application/json', 'Content-Length': Buffer.byteLength(body) } },
      r => { let d=''; r.on('data',c=>d+=c); r.on('end',()=>{ try{resolve(JSON.parse(d));}catch{reject(new Error('Parse'));} }); }
    );
    req.on('error', reject); req.write(body); req.end();
  });
}

function siteIndexPath(sub) { return path.join(WWW_ROOT, sub, 'index.html'); }
function siteExists(sub)    { try { return fs.existsSync(path.join(WWW_ROOT, sub)); } catch { return false; } }
function siteLastModified(sub) {
  try { return fs.statSync(siteIndexPath(sub)).mtime.toLocaleString('en-ZA'); } catch { return '—'; }
}
function readSite(sub)  { try { return fs.readFileSync(siteIndexPath(sub), 'utf8'); } catch { return ''; } }
function writeSite(sub, html) {
  fs.mkdirSync(path.join(WWW_ROOT, sub), { recursive: true });
  fs.writeFileSync(siteIndexPath(sub), html, 'utf8');
}

